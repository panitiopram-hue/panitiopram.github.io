/* ---------------- tryMultipleExts: โหลดรูปโดยลองหลายสกุล ---------------- */
function tryMultipleExts(imgEl, basePath) {
  const exts = ['.png', '.jpg', '.jpeg', '.webp'];
  let idx = 0;
  function handler() {
    imgEl.removeEventListener('error', handler);
    idx++;
    if (idx < exts.length) {
      imgEl.src = basePath + exts[idx];
      imgEl.addEventListener('error', handler);
    } else {
      imgEl.src = 'img/no-image.png';
      imgEl.dataset.fallback = 'true';
    }
  }
  imgEl.addEventListener('error', handler);
  imgEl.src = basePath + exts[0];
}

/* ---------------- clean & expandSteps ---------------- */
function cleanLeadingNumbers(s) {
  return s.replace(/^\s*\d+\s*[\.\)\-:\s]+\s*/, '').trim();
}

/* ---------------- ensureDetailedSteps ---------------- */
function ensureDetailedSteps(recipe) {
  const orig = Array.isArray(recipe.steps) ? recipe.steps.map(s => (s||'').toString().trim()).filter(Boolean) : [];

  // ถ้าขั้นตอนมีมากพอและละเอียดแล้ว ให้คืนค่าเดิม
  if (orig.length >= 3 && orig.every(step => step.length > 10)) {
    return orig.map(cleanLeadingNumbers);
  }

  // ถ้าขั้นตอนน้อยหรือไม่ละเอียดพอ ให้ปรับปรุง
  const detailedSteps = [];

  // เพิ่มขั้นตอนการเตรียมวัตถุดิบ
  if (recipe.ingredients && recipe.ingredients.length > 0) {
    detailedSteps.push('เตรียมวัตถุดิบทั้งหมดให้พร้อม: ' + recipe.ingredients.join(', '));
  }

  // เพิ่มขั้นตอนตามประเภทอุปกรณ์
  if (recipe.device === 'no-cook') {
    detailedSteps.push('ผสมส่วนผสมทั้งหมดในชามใหญ่');
    detailedSteps.push('คลุกเคล้าให้เข้ากันอย่างทั่วถึง');
    detailedSteps.push('ชิมรสชาติและปรับแต่งตามความชอบ');
  } else if (recipe.device === 'ไมโครเวฟ') {
    detailedSteps.push('จัดเรียงส่วนผสมในภาชนะทนความร้อน');
    detailedSteps.push('ตั้งเวลาและกำลังไฟตามสูตร');
    detailedSteps.push('ตรวจสอบความสุกก่อนนำออกจากไมโครเวฟ');
  } else if (recipe.device === 'หม้อหุงข้าว') {
    detailedSteps.push('ล้างและเตรียมวัตถุดิบให้เรียบร้อย');
    detailedSteps.push('เรียงส่วนผสมในหม้อหุงข้าวตามลำดับ');
    detailedSteps.push('กดปุ่มหุงและรอจนสุกได้ที่');
  } else if (recipe.device === 'กระทะ') {
    detailedSteps.push('ตั้งกระทะใช้ไฟกลาง ใส่น้ำมันเล็กน้อย');
    detailedSteps.push('ผัดส่วนผสมตามลำดับที่เหมาะสม');
    detailedSteps.push('ปรุงรสและผัดจนส่วนผสมสุกได้ที่');
  } else if (recipe.device === 'เตาอบ') {
    detailedSteps.push('เปิดเตาอบล่วงหน้าให้ร้อนตามอุณหภูมิที่กำหนด');
    detailedSteps.push('จัดเรียงส่วนผสมในภาชนะทนความร้อน');
    detailedSteps.push('อบจนสุกได้ที่ตามเวลาที่แนะนำ');
  } else {
    // อุปกรณ์ไม่ระบุชัด ให้เพิ่มขั้นตอนทั่วไป
    detailedSteps.push('เตรียมและปรุงอาหารตามขั้นตอนพื้นฐานที่เหมาะสมกับวัตถุดิบ');
  }

  // เพิ่มขั้นตอนการเสิร์ฟ
  detailedSteps.push('จัดจานและเสิร์ฟขณะร้อนหรือตามความเหมาะสม');

  return detailedSteps.map(cleanLeadingNumbers);
}

function expandSteps(recipe) {
  return ensureDetailedSteps(recipe);
}

