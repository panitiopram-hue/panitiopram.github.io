/* Render category pills */
function renderChooser() {
  if (!chooserEl) return;
  chooserEl.innerHTML = '';
  const all = document.createElement('div'); all.className = 'pill active'; all.textContent = 'ทั้งหมด';
  all.onclick = () => { activeCategory = null; updatePills(); renderGrid(filteredRecipes()); };
  chooserEl.appendChild(all);
  categories.forEach(cat => {
    const el = document.createElement('div'); el.className = 'pill'; el.textContent = cat;
    el.onclick = () => { activeCategory = cat; activeRecommended = false; if (recommendedBtn) recommendedBtn.classList.remove('active'); updatePills(); renderGrid(filteredRecipes()); };
    chooserEl.appendChild(el);
  });
}
function updatePills() {
  if (!chooserEl) return;
  Array.from(chooserEl.children).forEach(ch => ch.classList.remove('active'));
  if (!activeCategory) chooserEl.children[0].classList.add('active');
  else Array.from(chooserEl.children).forEach(ch => { if (ch.textContent === activeCategory) ch.classList.add('active'); });
}

/* Device filter handlers */
